function make_game (tilesize){
    const mapManager = new TileManager();
    const ovw_map = new Map(new mapaArray().get(), mapManager, tilesize);

    const genMap = (imglist) => {
        mapManager.set(0, new Tile(imglist["Img/grass/grama.jpg"], "grama"));
        mapManager.set(1, new Tile(imglist["Img/sand/sand.png"], "crate"));
        mapManager.set(3, new Tile(imglist["Img/water/water1.jpg"], "water"));
        ovw_map.generateMap();
    };
    loadSprites(genMap, "Img/sand/sand.png", "Img/grass/grama.jpg", "Img/water/water1.jpg");
    return ovw_map
}

